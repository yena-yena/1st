#7.16 터틀그래픽
import turtle as t
t.shape("classic")
#e
t.penup()
t.goto(-250,200)
t.pendown()
t.forward(50)
t.goto(-250,200)
t.pendown()
t.goto(-250,100)
t.forward(50)
t.penup()
t.goto(-250,150)
t.pendown()
t.forward(50)
#x
t.penup()
t.goto(-150,200)
t.pendown()
t.goto(-100,100)
t.penup()
t.goto(-100,200)
t.pendown()
t.goto(-150,100)
#i
t.penup()
t.goto(-50,200)
t.pendown()
t.right(90)
t.forward(100)
#t
t.penup()
t.goto(0,200)
t.pendown()
t.left(90)
t.forward(80)
t.penup()
t.goto(40,200)
t.right(90)
t.pendown()
t.forward(100)
t.done()