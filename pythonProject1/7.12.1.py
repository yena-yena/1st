import turtle as t
t.penup()
t.goto(-100,100)
t.pendown()
t.color('blue')
t.circle(50)
t.penup()
t.goto(-50,50)
t.color('yellow')
t.pendown()
t.circle(50)
t.penup()
t.goto(0,100)
t.color('black')
t.pendown()
t.circle(50)
t.penup()
t.goto(50,50)
t.color('green')
t.pendown()
t.circle(50)
t.penup()
t.goto(100,100)
t.color('red')
t.pendown()
t.circle(50)
t.done()